"""
描述
给定一个正整数 N，初始时有数值 X=1。你可以进行若干次如下操作：
选择一个整数 K (2 ≤ K)，支付 K 单位代价后，将 X 变为 K×X。

请你计算，将 X 恰好变为 N 所需要支付的最小总代价。

输入描述：
在一行上输入一个整数 N (1 ≤ N ≤ 3×10⁵)。

输出描述：
输出一个整数，代表最小总代价。

示例1
输入：12
输出：7
说明：对于 N=12，一种最优方案是依次选择 K=3（代价 3）与 K=4（代价 4），总代价 3+4=7；可以证明不存在代价更小的方案。

示例2
输入：1
输出：0
说明：对于 N=1，无需任何操作，总代价为 0。
"""


def solve() -> None:
    n = int(input())

    if n == 1:
        print(0)
        return

    ans = 0
    # 质因数分解
    x = n
    p = 2
    while p * p <= x:
        while x % p == 0:
            ans += p
            x //= p
        p += 1

    # 如果剩余 x > 1，则 x 本身是一个质因子
    if x > 1:
        ans += x

    print(ans)


if __name__ == "__main__":
    solve()
