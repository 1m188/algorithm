"""
描述
旺仔哥哥非常喜欢玩扫雷。现在给定一个初始的扫雷矩阵，其中用字符 `*` 表示雷，用 `.` 表示空白。
请你生成完整的扫雷矩阵。对于每个非雷格子，输出它周围八个方向中雷的个数；如果当前位置是雷，则仍输出 `*`。

【名词解释】
雷: 用 `*` 表示的地雷；
邻格: 与当前位置在八个方向上相邻的格子；
完整扫雷矩阵: 填写完相应数字后的矩阵。
输入描述：
在一行中输入两个整数
n
,
m
n,m
(
1
≦
n
,
m
≦
1000
)
(1≦n,m≦1000)，分别表示矩阵的行数和列数。
接下来
n
n 行，每行包含
m
m 个字符，每个字符是 `*` 或 `.`，无空格分隔，表示初始的扫雷矩阵。
输出描述：
输出
n
n 行
m
m 列，表示完整的扫雷矩阵。对于每个位置：

∙

∙如果该位置是雷，输出 `*`；
∙

∙否则输出一个数字，该数字等于该位置周围八个邻格中雷的个数。
示例1
输入：
4 4
....
..**
*.*.
.*.*
复制
输出：
0122
13**
*4*4
2*3*
复制
说明：
∙

∙第一行 `....` 中没有雷，对应 `0122`；
∙

∙第二行 `..**` 中两个雷，对应 `13**`；
∙

∙第三行 `*.*.` 中除自身外有 4 个雷邻格，对应 `*4*4`；
∙

∙第四行 `.*.*` 对应 `2*3*`。
示例2
输入：
3 5
*...*
.....
.*.*.
复制
输出：
*101*
22222
1*2*1
复制
说明：
∙

∙第一行 `*...*`：两个雷分别在两端，对应 `*101*`；
∙

∙第二行 `.....`：无雷，对应 `22222`；
∙

∙第三行 `.*.*.`：两个雷对中间格子有 2 个邻雷，对应 `1*2*1`。
"""

n, m = map(int, input().split())
mat = [list(input()) for _ in range(n)]
ans = [[0 for _ in range(m)] for _ in range(n)]
for i in range(n):
    for j in range(m):
        if mat[i][j] == "*":
            ans[i][j] = "*"
        else:
            cnt = 0
            for di in range(-1, 2):
                for dj in range(-1, 2):
                    if (
                        0 <= i + di < n
                        and 0 <= j + dj < m
                        and mat[i + di][j + dj] == "*"
                    ):
                        cnt += 1
            ans[i][j] = str(cnt)
for row in ans:
    print("".join(row))
