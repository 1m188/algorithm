MOD = 10**9 + 7


# 二维dp优化成一维dp
def bin_fib(n: int, m: int) -> int:
    li = [1 for _ in range(m + 1)]
    for _ in range(n - 1):
        li2 = [1 for _ in range(m + 1)]
        for i in range(2, m + 1):
            li2[i] = (li[i] + li2[i - 1]) % MOD
        li = li2
    return li[m]


n, m = map(int, input().strip().split())
print(bin_fib(n, m))
