T = int(input())
for _ in range(T):
    n = int(input())
    a = list(map(int, input().split()))

    x1 = max(a) - min(a)
    mean = sum(a) / n
    x2 = sum((a[i] - mean) ** 2 for i in range(n)) / n
    print(x1, f"{x2:.3f}")
