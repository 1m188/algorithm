#include <iostream>
using namespace std;

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);

    int n;
    cin >> n;

    long long prefix = 0;
    long long ans = 0;

    for (int i = 1; i <= n; i++) {
        int a;
        cin >> a;
        prefix += a;
        // 在位置j(>=2)合并，得分为前缀和P[j]
        // 最优策略：在所有P[j]>0的位置合并，答案为正前缀和之和
        if (i >= 2 && prefix > 0) {
            ans += prefix;
        }
    }

    cout << ans << endl;
    return 0;
}
