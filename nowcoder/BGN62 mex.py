import sys


def solve():
    data = sys.stdin.buffer.read().split()
    n = int(data[0])
    a = [int(data[i + 1]) for i in range(n)]

    distinct = set(a)

    if len(distinct) == 1:
        print(0)
        return

    if 0 not in distinct:
        print(-1)
        return

    # 答案 = max_value - num_distinct + 2
    # 每轮操作后，"间隙总数"恰好减少1，初始间隙 = max - (distinct-1)
    # 间隙归零后还需1轮使所有元素变为0
    print(max(distinct) - len(distinct) + 2)


if __name__ == "__main__":
    solve()
