/*
机器人数目

少年宫新近邮购了小机器人配件，共有3类，其中，
A类含有：8个轮子，1个传感器
B类含有: 6个轮子，3个传感器
C类含有：4个轮子，4个传感器

他们一共订购了100套机器人，收到了轮子600个，传感器280个。
根据这些信息请你计算：B类型机器人订购了多少个？
*/
public class _1 {
    public static void main(String[] args) {
        // A+B+C=100, 8A+6B+4C=600, A+3B+4C=280
        // 由前两个方程: 2A+B=100 → B=100-2A
        // 代入第三个: A+3B+4C=280, C=100-A-B=100-A-(100-2A)=A
        // A+3(100-2A)+4A=280 → A+300-6A+4A=280 → -A=-20 → A=20
        // B=100-2*20=60, C=20
        System.out.println(60);
    }
}
