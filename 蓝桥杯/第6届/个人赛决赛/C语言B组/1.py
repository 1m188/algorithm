"""

标题：积分之迷

小明开了个网上商店，卖风铃。共有3个品牌：A，B，C。
为了促销，每件商品都会返固定的积分。

小明开业第一天收到了三笔订单：
第一笔：3个A + 7个B + 1个C，共返积分：315
第二笔：4个A + 10个B + 1个C，共返积分：420
第三笔：A + B + C，共返积分....

你能算出第三笔订单需要返积分多少吗？

请提交该整数，不要填写任何多余的内容。


"""

# 解题思路：
#   设 A、B、C 每个品牌分别返积分 a、b、c。
#   第一笔：3a + 7b + c = 315  ... (1)
#   第二笔：4a + 10b + c = 420 ... (2)
#   第三笔：a + b + c = ?       ... (3)
#
#   (2) - (1): a + 3b = 105     ... (4)
#   代入 (1): 3(105 - 3b) + 7b + c = 315  =>  c = 2b
#   代入 (3): a + b + c = (105 - 3b) + b + 2b = 105
#   答案与 b 无关，恒为 105。


def solve() -> int:
    """通过方程组推导，直接返回第三笔订单的积分。"""
    # 由 (2) - (1) 得 a + 3b = 105
    # 由代入消元得 c = 2b
    # 因此 a + b + c = (105 - 3b) + b + 2b = 105
    return 105


if __name__ == "__main__":
    print(solve())
