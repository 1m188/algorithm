/*
 *
 * 标题：分机号
 *
 * X老板脾气古怪，他们公司的电话分机号都是3位数，老板规定，所有号码必须是降序排列，且不能有重复的数位。比如：
 *
 * 751,520,321 都满足要求，而，
 * 766,918,201 就不符合要求。
 *
 * 现在请你计算一下，按照这样的规定，一共有多少个可用的3位分机号码？
 *
 * 请直接提交该数字，不要填写任何多余的内容。
 *
 */

/**
 * 解法分析：
 * 从 0~9 共10个数字中任选3个不同的数字，按降序排列即唯一确定一个合法分机号。
 * 百位为最大数字，故不会出现前导零（0 若被选中必排在个位）。
 * 总数 = C(10,3) = 10×9×8 / 6 = 120。
 *
 * 以下通过三重循环枚举验证：百位 i (9→2)，十位 j (i-1→1)，个位 k (j-1→0)，
 * 满足 i>j>k 即为合法分机号，计数累加。
 */
public class _1 {
    public static void main(String[] args) {
        int count = 0;

        // 百位：9 到 2（百位为1时，十位/个位不够两个更小的不同数字）
        for (int i = 9; i >= 2; i--) {
            // 十位：比百位小的数字
            for (int j = i - 1; j >= 1; j--) {
                // 个位：比十位小的数字（可为0）
                for (int k = j - 1; k >= 0; k--) {
                    count++;
                }
            }
        }

        // 输出结果：120
        System.out.println(count);
    }
}