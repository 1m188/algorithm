/*
 * 解题思路：
 * X 的宽度 = n + m - 1（对角线长度 n，加上笔画外延 m-1）。
 *
 * 第 i 行（0 <= i < n）：
 *   左斜杠（主对角线方向）：从列 i 开始，输出 m 个 '*'。
 *   右斜杠（反对角线方向）：从列 (n-1-i) 开始，输出 m 个 '*'。
 *   其余位置输出 '.'。
 *   两斜杠可能在中部重叠，重叠处只输出一次 '*'。
 */
#include <iostream>

int main() {
    int m, n;
    std::cin >> m >> n;

    int width = n + m - 1; // X 的总宽度

    for (int i = 0; i < n; i++) {
        int left_start = i;          // 左斜杠起始列
        int right_start = n - 1 - i; // 右斜杠起始列
        for (int j = 0; j < width; j++) {
            // 当前列是否在左斜杠 / 右斜杠的笔画范围内
            bool in_left = (j >= left_start && j < left_start + m);
            bool in_right = (j >= right_start && j < right_start + m);
            if (in_left || in_right)
                std::cout << '*';
            else
                std::cout << '.';
        }
        std::cout << std::endl;
    }
    return 0;
}
