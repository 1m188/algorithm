/*

加法变乘法

我们都知道：1+2+3+ ... + 49 = 1225
现在要求你把其中两个不相邻的加号变成乘号，使得结果为2015

比如：
1+2+3+...+10*11+12+...+27*28+29+...+49 = 2015
就是符合要求的答案。

请你寻找另外一个可能的答案，并把位置靠前的那个乘号左边的数字提交（对于示例，就是提交10）。

注意：需要你提交的是一个整数，不要填写任何多余的内容。
*/

/*
 * 解题思路：
 * 原始总和 1~49 = 1225，需要增加到 2015，差额 = 790。
 * 将 i+(i+1) 变为 i*(i+1)，增量 = i^2-i-1。
 * 枚举两个不相邻位置（i < j-1），检查总增量是否为 790。
 * 排除示例解 (10, 27)，输出另一个的 i。
 */
#include <iostream>

int main() {
    const int TARGET_DELTA = 2015 - 1225; // 790

    for (int i = 1; i <= 46; i++) {         // 第一个乘号左边数字
        int d1 = i * i - i - 1;             // 第一处变乘带来的增量
        for (int j = i + 2; j <= 48; j++) { // 不相邻：j >= i+2
            int d2 = j * j - j - 1;         // 第二处变乘带来的增量
            if (d1 + d2 == TARGET_DELTA) {
                if (i != 10) { // 排除示例 (10, 27)
                    std::cout << i << std::endl;
                    return 0;
                }
            }
        }
    }
    return 0;
}
