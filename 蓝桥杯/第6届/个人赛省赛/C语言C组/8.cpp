/*
 * 解题思路：
 * 初始买入 n 瓶，全部喝完得到 n 个瓶盖。
 * 每 3 个瓶盖可兑换 1 瓶，兑换后瓶盖数 = 兑换的瓶数 + 剩余的瓶盖（<3）。
 * 循环直到瓶盖数不足 3。
 *
 * 等价公式：总瓶数 = n + (n - 1) / 2
 * （每 2 个额外瓶盖可通过兑换再产生 1 瓶 + 1 个新盖的循环获得 1 瓶）
 */
#include <iostream>

int main() {
    int n;
    std::cin >> n;

    int total = n; // 初始买到的饮料
    int caps = n;  // 初始瓶盖数

    while (caps >= 3) {
        int exchanged = caps / 3;    // 可兑换的瓶数
        total += exchanged;          // 喝掉兑换的饮料
        caps = exchanged + caps % 3; // 新瓶盖 = 兑换的瓶 + 剩余旧盖
    }

    std::cout << total << std::endl;
    return 0;
}
