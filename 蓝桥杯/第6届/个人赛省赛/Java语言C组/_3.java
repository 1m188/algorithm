/*
无穷分数

无穷的分数，有时会趋向于固定的数字。
请计算【图1.jpg】所示的无穷分数，要求四舍五入，精确到小数点后5位，小数位不足的补0。

请填写该浮点数，不能填写任何多余的内容。
*/

/*
 * 解题思路：
 * 图1所示无穷分数为无限连分数：
 *           1
 * x = ------------- = 1/(1 + x)
 *       1 +    1
 *           -------
 *           1 + ...
 *
 * 解方程 x² + x - 1 = 0，得 x = (√5 - 1)/2 ≈ 0.6180339887...
 * 四舍五入到小数点后 5 位：0.61803。
 * 迭代法验证：从 x=1 开始反复计算 x = 1/(1+x)，收敛到同一值。
 */

public class _3 {
    public static void main(String[] args) {
        double x = 1.0;
        // 迭代足够多次直到收敛
        for (int i = 0; i < 100; i++)
            x = 1.0 / (1.0 + x);
        System.out.printf("%.5f\n", x);
    }
}
