/*
三角形面积

如【图1】所示。图中的所有小方格面积都是1。
那么，图中的三角形面积应该是多少呢？

请填写三角形的面积。不要填写任何多余内容或说明性文字。
*/

/*
 * 解题思路：
 * 图1中三角形顶点位于方格交点上，采用坐标法或割补法求面积。
 * 设三角形三个顶点的坐标为 (x1,y1), (x2,y2), (x3,y3)，
 * 面积 = |x1(y2-y3)+x2(y3-y1)+x3(y1-y2)| / 2。
 * 根据原图可测得：三角形面积 = 方形面积(8×8=64) − 三个直角三角形面积
 *        = 64 − (8+20+8) = 64 − 36 = 28。
 * 答案：28
 */

public class _1 {
    public static void main(String[] args) {
        // 图1中三角形：
        // 包含在一个 8×8 的方形区域内（面积 64）
        // 方形减去三角形周围的三个直角三角形：
        //   三角形A: 底4高4 → 面积 4×4/2 = 8
        //   三角形B: 底8高5 → 面积 8×5/2 = 20
        //   三角形C: 底4高4 → 面积 4×4/2 = 8
        // 三角形面积 = 64 - 8 - 20 - 8 = 28
        int area = 64 - 8 - 20 - 8;
        System.out.println(area);
    }
}
