/*
加法变乘法

我们都知道：1+2+3+ ... + 49 = 1225
现在要求你把其中两个不相邻的加号变成乘号，使得结果为2015

比如：
1+2+3+...+10*11+12+...+27*28+29+...+49 = 2015
就是符合要求的答案。

请你寻找另外一个可能的答案，并把位置靠前的那个乘号左边的数字提交（对于示例，就是提交10）。

注意：需要你提交的是一个整数，不要填写任何多余的内容。
*/

/*
 * 解题思路：
 * 原始总和 1~49 = 1225，需增加到 2015，差 790。
 * 将 i+(i+1) 变为 i*(i+1)，增量 = i^2 - i - 1。
 * 枚举两个不相邻位置 i < j-1，检查总增量是否 = 790。
 * 排除示例解 (10, 27)，输出另一个 i = 16。
 */

public class _6 {
    public static void main(String[] args) {
        final int TARGET = 2015 - 1225; // 790
        for (int i = 1; i <= 46; i++) {
            int d1 = i * i - i - 1;
            for (int j = i + 2; j <= 48; j++) {
                int d2 = j * j - j - 1;
                if (d1 + d2 == TARGET) {
                    if (i != 10) {
                        System.out.println(i);
                        return;
                    }
                }
            }
        }
    }
}
