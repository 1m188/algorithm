/*

牌型种数

小明被劫持到X赌城，被迫与其他3人玩牌。
一副扑克牌（去掉大小王牌，共52张），均匀发给4个人，每个人13张。
这时，小明脑子里突然冒出一个问题：
如果不考虑花色，只考虑点数，也不考虑自己得到的牌的先后顺序，自己手里能拿到的初始牌型组合一共有多少种呢？

请填写该整数，不要填写任何多余的内容或说明文字。
*/

/*
 * 解题思路：
 * 共 13 种点数（A,2,...,K），每种点数最多有 4 张（4 种花色）。
 * 不考虑花色和顺序，即在每种点数中选 0~4 张，使总张数恰好为 13。
 * 用深度优先搜索枚举每种点数的选取张数并统计合法方案。
 */
#include <iostream>

int cnt = 0; // 合法方案总数

// k: 当前考虑的点数（0~12），sum: 已选取的总张数
void dfs(int k, int sum) {
    if (k == 13) {            // 13 种点数全部处理完毕
        if (sum == 13) cnt++; // 恰好 13 张为一组合法牌型
        return;
    }
    // 第 k 种点数可取 0~4 张，同时保证总张数不超过 13
    for (int pick = 0; pick <= 4 && sum + pick <= 13; pick++)
        dfs(k + 1, sum + pick);
}

int main() {
    dfs(0, 0);
    std::cout << cnt << std::endl;
    return 0;
}
