/*
星系炸弹

在X星系的广袤空间中漂浮着许多X星人造"炸弹"，用来作为宇宙中的路标。
每个炸弹都可以设定多少天之后爆炸。
比如：阿尔法炸弹2015年1月1日放置，定时为15天，则它在2015年1月16日爆炸。
有一个贝塔炸弹，2014年11月9日放置，定时为1000天，请你计算它爆炸的准确日期。

请填写该日期，格式为 yyyy-mm-dd  即4位年份2位月份2位日期。比如：2015-02-19
请严格按照格式书写。不能出现其它文字或符号。
*/

/*
 * 解题思路：
 * 从 2014-11-09 开始，累加 1000 天。
 * 2014-11-09 → 2014-12-31：11 月余 21 天 + 12 月全 31 天 = 52 天
 * 剩余：1000 - 52 = 948
 * 2015 年：365 天 → 剩余 948 - 365 = 583
 * 2016 年（闰年）：366 天 → 剩余 583 - 366 = 217
 * 2017 年：1 月 31 + 2 月 28 + 3 月 31 + 4 月 30 + 5 月 31 + 6 月 30 + 7 月 31 = 212
 * 剩余 217 - 212 = 5 → 8 月 5 日
 * 答案：2017-08-05
 */

public class _2 {
    public static void main(String[] args) {
        int y = 2014, m = 11, d = 9;
        int days = 1000;

        int[] monthDays = {0, 31, 28, 31, 30, 31, 30, 31, 31, 30, 31, 30, 31};

        while (days > 0) {
            // 闰年修改 2 月天数
            if ((y % 4 == 0 && y % 100 != 0) || y % 400 == 0)
                monthDays[2] = 29;
            else
                monthDays[2] = 28;

            int remain = monthDays[m] - d + 1; // 当月剩余天数（含今天）
            if (days < remain) {
                d += days;
                days = 0;
            } else {
                days -= remain;
                d = 1;
                m++;
                if (m > 12) {
                    m = 1;
                    y++;
                }
            }
        }

        System.out.printf("%d-%02d-%02d\n", y, m, d);
    }
}
