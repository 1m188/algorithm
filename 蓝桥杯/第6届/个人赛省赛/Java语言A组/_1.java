/*
熊怪吃核桃

森林里有一只熊怪，很爱吃核桃。不过它有个习惯，每次都把找到的核桃分成相等的两份，吃掉一份，留一份。如果不能等分，熊怪就会扔掉一个核桃再分。第二天再继续这个过程，直到最后剩一个核桃了，直接丢掉。

有一天，熊怪发现了1543个核桃，请问，它在吃这些核桃的过程中，一共要丢掉多少个核桃。

请填写该数字（一个整数），不要填写任何多余的内容或说明文字。
*/

/*
 * 解题思路：
 * 模拟熊怪吃核桃的过程。
 * 每天：若核桃数为偶数，吃掉一半（留下另一半），不丢弃。
 *       若核桃数为奇数，丢弃 1 个，然后平分，吃掉一半（留下另一半）。
 * 直到剩余 1 个，直接丢弃，过程结束。
 * 统计总共丢弃的核桃数。
 */

public class _1 {
    public static void main(String[] args) {
        int n = 1543; // 初始核桃数
        int drop = 0; // 总丢弃数
        while (n > 1) {
            if (n % 2 == 0) {
                // 偶数：吃掉一半，留下另一半，不丢弃
                n /= 2;
            } else {
                // 奇数：丢弃 1 个，然后平分，留下一半
                drop++;
                n = (n - 1) / 2;
            }
        }
        // 最后剩 1 个，直接丢弃
        drop++;
        System.out.println(drop);
    }
}
