/*

标题：数字划分


w星球的长老交给小明一个任务：
1,2,3...16 这16个数字分为两组。
要求：
这两组数字的和相同，
并且，两组数字的平方和也相同，
并且，两组数字的立方和也相同。

请你利用计算机的强大搜索能力解决这个问题。
并提交1所在的那个分组的所有数字。

这些数字要从小到大排列，两个数字间用一个空格分开。
即类似：1 4 5 8 ...  这样的答案。

注意，只提交这一组数字，不要填写任何多余的内容。

----------------------------------------
笨笨有话说：
    只要一个组的成员确定了，另一个组的成员也就确定了。枚举一个组的成员就可以了。
        凭直觉，两个组的成员数目不会差太多吧。
歪歪有话说：
    既然求 1 所在的那个组，那只要枚举剩余的成员就可以了。
        貌似都是8个成员的可能性很大啊。

*/

#include <iostream>
#include <vector>

using namespace std;

int main() {
    // 1..16 的总和 = 136，每组 68
    // 1..16 的平方和 = 1496，每组 748
    // 1..16 的立方和 = 18496，每组 9248
    // 1 所在组还需 sum=67, sq=747, cu=9247

    int nums[15]; // 2..16
    for (int i = 0; i < 15; i++)
        nums[i] = i + 2;

    // 枚举 2..16 的所有子集（2^15 = 32768）
    for (int mask = 0; mask < (1 << 15); mask++) {
        vector<int> group;
        group.push_back(1); // 1 必然在此组
        int sum = 1, sq = 1, cu = 1;

        for (int i = 0; i < 15; i++) {
            if (mask & (1 << i)) {
                int x = nums[i];
                group.push_back(x);
                sum += x;
                sq += x * x;
                cu += x * x * x;
            }
        }

        if (sum == 68 && sq == 748 && cu == 9248) {
            // 输出结果，空格分隔
            for (size_t i = 0; i < group.size(); i++) {
                if (i > 0) cout << " ";
                cout << group[i];
            }
            cout << endl;
        }
    }

    return 0;
}
