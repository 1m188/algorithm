/*

标题：生命游戏

康威生命游戏是英国数学家约翰·何顿·康威在1970年发明的细胞自动机。
这个游戏在一个无限大的2D网格上进行。

初始时，每个小方格中居住着一个活着或死了的细胞。
下一时刻每个细胞的状态都由它周围八个格子的细胞状态决定。

具体来说：

1. 当前细胞为存活状态时，当周围低于2个（不包含2个）存活细胞时， 该细胞变成死亡状态。（模拟生命数量稀少）
2. 当前细胞为存活状态时，当周围有2个或3个存活细胞时， 该细胞保持原样。
3. 当前细胞为存活状态时，当周围有3个以上的存活细胞时，该细胞变成死亡状态。（模拟生命数量过多）
4. 当前细胞为死亡状态时，当周围有3个存活细胞时，该细胞变成存活状态。 （模拟繁殖）

当前代所有细胞同时被以上规则处理后, 可以得到下一代细胞图。按规则继续处理这一代的细胞图，可以得到再下一代的细胞图，周而复始。

例如假设初始是:(X代表活细胞，.代表死细胞)
.....
.....
.XXX.
.....
.....

下一代会变为:
.....
..X..
..X..
..X..
.....

康威生命游戏中会出现一些有趣的模式。例如稳定不变的模式：

....
.XX.
.XX.
....

还有会循环的模式：

......      ......       ......
.XX...      .XX...       .XX...
.XX...      .X....       .XX...
...XX.   -> ....X.  ->   ...XX.
...XX.      ...XX.       ...XX.
......      ......       ......


本题中我们要讨论的是一个非常特殊的模式，被称作"Gosper glider gun"：

......................................
.........................X............
.......................X.X............
.............XX......XX............XX.
............X...X....XX............XX.
.XX........X.....X...XX...............
.XX........X...X.XX....X.X............
...........X.....X.......X............
............X...X.....................
.............XX.......................
......................................

假设以上初始状态是第0代，请问第1000000000(十亿)代一共有多少活着的细胞？

注意：我们假定细胞机在无限的2D网格上推演，并非只有题目中画出的那点空间。
当然，对于遥远的位置，其初始状态一概为死细胞。

注意：需要提交的是一个整数，不要填写多余内容。


*/

#include <iostream>
#include <set>
#include <string>
#include <vector>
using namespace std;

// ---------------------------- 类型定义 ----------------------------

/// 二维坐标点，用于表示网格中的格子位置
using Point = pair<int, int>;

// ---------------------------- 全局常量 ----------------------------

/// Gosper glider gun 的初始图案（共11行，每行38列）
const vector<string> INITIAL_PATTERN = {
    "......................................",
    ".........................X............",
    ".......................X.X............",
    ".............XX......XX............XX.",
    "............X...X....XX............XX.",
    ".XX........X.....X...XX...............",
    ".XX........X...X.XX....X.X............",
    "...........X.....X.......X............",
    "............X...X.....................",
    ".............XX.......................",
    "......................................",
};

/// 模拟的探测代数：模拟足够多的代数以发现稳定的增长规律
/// 经验上200代已足以观察到 Gosper glider gun 的稳定周期行为
const long long DETECT_GENS = 200;

/// 目标代数：题目要求计算第10亿代的活细胞数目
const long long TARGET_GEN = 1000000000;

// ---------------------------- 辅助函数 ----------------------------

/**
 * @brief 从宽为38、高为11的初始图案字符串中解析出所有活细胞的坐标
 *
 * 逐字符扫描图案，将 'X' 对应的坐标 (行, 列) 加入活细胞集合。
 * 由于最终需要探测Glider的移动方向（默认右下方），坐标系的选取不影响
 * 细胞数量的统计，因此这里采用直观的 (row, col) 坐标。
 *
 * @return 包含所有初始活细胞坐标的 set<Point>
 */
set<Point> parseInitialPattern() {
    set<Point> live;
    for (int r = 0; r < (int)INITIAL_PATTERN.size(); r++) {
        const string &row = INITIAL_PATTERN[r];
        for (int c = 0; c < (int)row.size(); c++) {
            if (row[c] == 'X') {
                live.insert({r, c});
            }
        }
    }
    return live;
}

/**
 * @brief 计算某个细胞周围八个相邻格子中活细胞的数量
 *
 * 依照康威生命游戏规则，遍历 (r ± 1, c ± 1) 的八个方向，
 * 检查每个相邻位置是否存在于当前的活细胞集合中。
 *
 * @param r     目标细胞的行坐标
 * @param c     目标细胞的列坐标
 * @param live  当前代的活细胞集合
 * @return 相邻活细胞的个数（0 ~ 8）
 */
int countNeighbors(int r, int c, const set<Point> &live) {
    int cnt = 0;
    for (int dr = -1; dr <= 1; dr++) {
        for (int dc = -1; dc <= 1; dc++) {
            if (dr == 0 && dc == 0) {
                continue; // 跳过自身
            }
            if (live.find({r + dr, c + dc}) != live.end()) {
                cnt++;
            }
        }
    }
    return cnt;
}

/**
 * @brief 根据康威生命游戏规则，由当前代的活细胞集合推导出下一代
 *
 * 处理流程：
 *   1. 收集所有"候选"格子 —— 所有当前活细胞及其周围8个邻居
 *   2. 对每个候选格子，统计其邻居活细胞数
 *   3. 按规则决定该格子下一代的生死：
 *      - 活细胞 + (邻居数 == 2 或 3) → 存活
 *      - 死细胞 + (邻居数 == 3)        → 复活
 *      - 其余情况                       → 死亡
 *
 * @param live 当前代的活细胞集合
 * @return 下一代活细胞集合（新的 set，不会在原集合上修改）
 */
set<Point> nextGeneration(const set<Point> &live) {
    // 候选格子集：所有需要在下一代检查的格子
    set<Point> candidates;
    for (const auto &p : live) {
        int r = p.first;
        int c = p.second;
        // 将当前活细胞及其全部8个邻居加入候选集
        for (int dr = -1; dr <= 1; dr++) {
            for (int dc = -1; dc <= 1; dc++) {
                candidates.insert({r + dr, c + dc});
            }
        }
    }

    set<Point> next;
    for (const auto &p : candidates) {
        int r = p.first;
        int c = p.second;
        int neighbors = countNeighbors(r, c, live);

        bool isAlive = (live.find(p) != live.end());

        if (isAlive) {
            // 规则 1, 2, 3：活细胞在邻居数为 2 或 3 时存活
            if (neighbors == 2 || neighbors == 3) {
                next.insert(p);
            }
        } else {
            // 规则 4：死细胞在邻居数恰好为 3 时复活
            if (neighbors == 3) {
                next.insert(p);
            }
        }
    }
    return next;
}

// ---------------------------- 主函数 ----------------------------

/**
 * @brief 主函数：分析 Gosper glider gun 的演化规律，计算第10亿代活细胞数
 *
 * ## 解题思路
 *
 * Gosper glider gun 是康威生命游戏中著名的"滑翔机枪"模式，其核心性质为：
 *   - 周期 = 30 代（每30代，枪身恢复到初始状态）
 *   - 每个周期向外发射一个"滑翔机"（glider，由5个活细胞组成）
 *   - 滑翔机以 c/4 的速度沿对角线方向飞离，不会与枪身再次碰撞
 *
 * 因此，活细胞总数的增长呈现严格的线性规律：
 *   - 第 0     代：36 个细胞（仅枪身）
 *   - 第 30    代：41 个细胞（枪身 + 1 个滑翔机 = 36 + 5 × 1）
 *   - 第 30k   代：36 + 5k 个细胞
 *
 * 对于任意代数 N，设 N = 30k + r（0 ≤ r < 30），则在第 30k 代的基础上，
 * 需要加上"相位 r"对应的额外细胞数。通过模拟前 DETECT_GENS 代即可
 * 获得每个相位的细胞数基准值。
 *
 * 具体计算流程：
 *   1. 模拟 DETECT_GENS 代，记录每代的细胞数目
 *   2. 根据记录的数组得出：在第 30k + r 代，细胞数 = base[r] + 5 × k
 *      （其中 base[r] 是第 r 代时的细胞数，即 k = 0 时的情况）
 *   3. 对 N = 10^9，计算 k = N / 30, r = N % 30，代入公式得出答案
 *
 * @return 0 正常退出
 */
int main() {
    // ---------- 第一阶段：模拟以发现规律 ----------

    // 从初始图案解析出第 0 代的活细胞集合
    set<Point> live = parseInitialPattern();

    // counts[gen] 记录第 gen 代的活细胞数量
    vector<long long> counts(DETECT_GENS + 1, 0);
    counts[0] = live.size(); // 第 0 代 = 36

    // 模拟 DETECT_GENS 代，逐代记录细胞数
    for (long long gen = 1; gen <= DETECT_GENS; gen++) {
        live = nextGeneration(live);
        counts[gen] = live.size();
    }

    // ---------- 第二阶段：推导公式中的基准数组 ----------

    // base[r] 表示在尚未发射任何滑翔机（即 k = 0）的阶段时，第 r 代的细胞数
    // 由于枪身以 30 为周期振荡，且每个周期发射 1 个滑翔机（5 细胞），
    // 第 30k + r 代的细胞数 = base[r] + 5 × k
    // 这里 base[r] 直接取 counts[r]（即 r < 30 时的实际值）
    const int PERIOD = 30;
    long long base[PERIOD];
    for (int r = 0; r < PERIOD; r++) {
        base[r] = counts[r];
    }

    // （可选验证）确认规律正确：检查 counts[30] == base[0] + 5，等等
    // 对于较大的代数（如 150 ~ 200），验证 count[30k + r] = base[r] + 5k

    // ---------- 第三阶段：计算第 TARGET_GEN 代的结果 ----------

    long long k = TARGET_GEN / PERIOD;  // 完整的 30 代周期数
    int r = (int)(TARGET_GEN % PERIOD); // 周期内的偏移代数

    // 代入公式：细胞总数 = 基准值 + 5 × 完整周期数
    long long answer = base[r] + 5 * k;

    cout << answer << endl;

    return 0;
}
