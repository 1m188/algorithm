/*

标题：平方十位数

由0~9这10个数字不重复、不遗漏，可以组成很多10位数字。
这其中也有很多恰好是平方数（是某个数的平方）。

比如：1026753849，就是其中最小的一个平方数。

请你找出其中最大的一个平方数是多少？

注意：你需要提交的是一个10位数字，不要填写任何多余内容。

*/

#include <cmath>
#include <cstring>
#include <iostream>
using namespace std;

/**
 * @brief 判断一个长整型数是否恰好由0~9这10个数字各出现一次组成
 *
 * 使用一个位掩码计数器来追踪0~9每个数字是否出现。
 * 10位数字中不允许有重复数字，每一位都不能缺失。
 *
 * @param n 待判断的10位正整数
 * @return true  若n的每一位恰好是0~9各出现一次（不重复、不遗漏）
 * @return false 否则
 */
bool isValid(long long n) {
    int digits = 0;    // 位掩码：第k位为1表示数字k已出现过
    int totalBits = 0; // 统计出现的不同数字个数（防止数字0出现在开头导致掩码遗漏判断）

    while (n > 0) {
        int d = n % 10;   // 取出当前最低位数字
        int bit = 1 << d; // 数字d对应的掩码位

        if (digits & bit) { // 如果该数字已经出现过，则不符合"不重复"要求
            return false;
        }

        digits |= bit; // 标记该数字为已出现
        totalBits++;   // 计数器+1
        n /= 10;       // 右移一位
    }

    // 如果恰好出现了10个不同的数字，且掩码覆盖了0~9全部（掩码 = 0b1111111111 = 1023）
    return totalBits == 10 && digits == 1023;
}

/**
 * @brief 主函数：找出由0~9不重复不遗漏组成的最大平方十位数
 *
 * 思路：
 * 1. 确定搜索范围：平方数必须是10位数，设其为x^2，则 x ∈ [sqrt(10^9), sqrt(10^10 - 1)]
 *    即 x ∈ [31623, 99999]（31623^2 = 999950129 是9位数，需要调整）
 *    精确计算：最小10位数 1023456789 的平方根约为 31992
 *             最大10位数 9876543210 的平方根约为 99380
 * 2. 从大到小遍历 x，找到第一个满足"每位数字0~9各出现一次"的 x^2，即为最大解
 *
 * @return 0 正常退出
 */
int main() {
    // 计算搜索范围的端点：sqrt(最小的可能10位数) 到 sqrt(最大的可能10位数)
    long long minNum = 1023456789LL; // 由0~9组成的最小10位数（0不能开头，所以是1023456789）
    long long maxNum = 9876543210LL; // 由0~9组成的最大10位数

    // 搜索下限与上限：分别向上取整和向下取整
    long long lower = (long long)sqrt(minNum); // ≈ 31992
    long long upper = (long long)sqrt(maxNum); // ≈ 99380

    // 从大到小遍历，一旦找到即输出并结束（保证结果为最大）
    for (long long x = upper; x >= lower; x--) {
        long long square = x * x;

        // 剪枝：平方数的位数不足10位（例如 x < 31623 时）直接跳过
        if (square < 1000000000LL) {
            continue;
        }

        // 判断该平方数是否恰好由0~9各出现一次组成
        if (isValid(square)) {
            cout << square << endl; // 输出结果（最大的平方十位数）
            return 0;
        }
    }

    return 0;
}
