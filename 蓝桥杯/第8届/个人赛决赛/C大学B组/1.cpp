/*
 * 标题：36进制
 *
 * 【题目描述】
 *   对于16进制，我们使用字母A-F来表示10及以上的数字。
 *   如法炮制，一直用到字母Z，就可以表示36进制。
 *
 *   36进制中，A表示10，Z表示35，AA表示370
 *
 *   你能算出 MANY 表示的数字用10进制表示是多少吗?
 *
 *   请提交一个整数，不要填写任何多余的内容（比如，说明文字）
 *
 * 【求解思路】
 *   36 进制规则：A=10, B=11, ..., Z=35。
 *   将字符串 "MANY" 按照 36 进制转换为十进制：
 *     M=22, A=10, N=23, Y=34
 *     MANY = 22×36³ + 10×36² + 23×36¹ + 34×36⁰ = 1040254
 */

#include <iostream>
#include <string>
using namespace std;

int main() {
    // 待转换的 36 进制字符串
    string s = "MANY";

    // 累加结果
    long long result = 0;

    // 从高位到低位逐位转换（秦九韶算法 / 霍纳法则）
    for (char c : s) {
        // 将字母转换为对应的数值（A=10, B=11, ..., Z=35）
        int digit = c - 'A' + 10;
        result = result * 36 + digit;
    }

    // 输出十进制结果
    cout << result << endl;

    return 0;
}
