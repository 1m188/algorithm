/*
 * ============================================================
 * 完整可编译运行的版本（填空答案已填入：m - y + 1）
 * ============================================================
 *
 * 算法思路：
 *   希尔伯特曲线 Hn 覆盖 2^n × 2^n 的网格，可用分治递归求解。
 *   将网格均分为四个 m×m 的象限（m = 2^(n-1)），根据 Hn 的构造规则：
 *
 *   - 左下角 (x <= m, y <= m)：Hn-1 顺时针旋转 90° 放置
 *     坐标映射为 (x_h, y_h) = (y, x)，即交换 xy，偏移为 0。
 *
 *   - 左上角 (x <= m, y > m)：Hn-1 标准方向放置
 *     坐标映射为 (x_h, y_h) = (x, y - m)，偏移为 1 * m²。
 *
 *   - 右上角 (x > m, y > m)：Hn-1 标准方向放置
 *     坐标映射为 (x_h, y_h) = (x - m, y - m)，偏移为 2 * m²。
 *
 *   - 右下角 (x > m, y <= m)：Hn-1 逆时针旋转 90° 放置
 *     坐标映射为 (x_h, y_h) = (m - y + 1, m * 2 - x + 1)，偏移为 3 * m²。
 *     推导：逆时针旋转 90° 后，原 Hn-1 的起点 (1,1) 映射到右下角象限的
 *     入口 (2m, m)，终点 (m,1) 映射到出口即整条曲线终点 (2m, 1)。
 *     反解可得 x_h = m - y + 1（y 轴反向），y_h = 2m - x + 1（x 轴反向）。
 *
 *   递归基：n == 0 时只有一个点，序号为 1。
 */

#include <stdio.h>

long long f(int n, int x, int y) {
    if (n == 0) return 1;
    int m = 1 << (n - 1); /* 半边长：2^(n-1) */

    if (x <= m && y <= m) {
        /* 左下角：顺时针旋转 90°，交换 x,y */
        return f(n - 1, y, x);
    }
    if (x > m && y <= m) {
        /* 右下角：逆时针旋转 90°，x/y 均反向映射 */
        return 3LL * m * m + f(n - 1, m - y + 1, m * 2 - x + 1);
    }
    if (x <= m && y > m) {
        /* 左上角：标准方向，y 坐标向下平移 m */
        return 1LL * m * m + f(n - 1, x, y - m);
    }
    /* if (x > m && y > m) */
    /* 右上角：标准方向，x,y 均向下/左平移 m */
    return 2LL * m * m + f(n - 1, x - m, y - m);
}

int main() {
    int n, x, y;
    scanf("%d %d %d", &n, &x, &y);
    printf("%lld\n", f(n, x, y));
    return 0;
}
