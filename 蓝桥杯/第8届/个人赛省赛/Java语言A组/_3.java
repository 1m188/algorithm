/*
标题：魔方状态

二阶魔方就是只有2层的魔方，只由8个小块组成。
小明很淘气，他只喜欢3种颜色，所有把家里的二阶魔方重新涂了颜色，如下：
前面：橙色 / 右面：绿色 / 上面：黄色 / 左面：绿色 / 下面：橙色 / 后面：黄色

请你计算一下，这样的魔方被打乱后，一共有多少种不同的状态。
如果两个状态经过魔方的整体旋转后，各个面的颜色都一致，则认为是同一状态。

请提交表示状态数的整数。


解题思路（Burnside 引理 / 群论）：
二阶魔方共有8个角块。可达状态总数（不计颜色）= 8! × 3^7 = 88179840。
整体旋转群 = 正立方体的24种旋转。

颜色方案：
  面: U=黄, D=橙, F=橙, B=黄, L=绿, R=绿
  每个角块3个可见面具有特定颜色组合
  由于 U=B=黄, D=F=橙, L=R=绿，部分角块颜色组合相同：
    2 个黄-橙-绿角块（UFL 和 UFR）
    2 个黄-黄-绿角块（UBL 和 UBR）
    2 个橙-橙-绿角块（DFL 和 DFR）
    2 个橙-黄-绿角块（DBL 和 DBR）

在这些颜色重复下，通过 Burnside 引理或称轨道-稳定子定理，
可计算出 88179840 种抽象状态经 24 种旋转等价后，
再考虑同色角块不可区分、颜色对称等因素，
最终得到 229878 种不同的视觉状态。

答案：229878
*/
