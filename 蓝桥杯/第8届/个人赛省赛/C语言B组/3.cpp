/*
 * 解题思路：
 * 金字塔共 30 层，前 29 层为已知重量，第 30 层为电子秤。
 * 每个金属块的重量精确平分到下方两个块上。
 *
 * 用 double 数组模拟分配过程：
 * a[i][j] 为第 i 层第 j 个位置的总重量。
 * 对每个 a[i][j]，将其一半加到 a[i+1][j]，一半加到 a[i+1][j+1]。
 *
 * 处理完毕后，底行得到 30 个重量值。
 * 已知其中的最小值对应示数 2086458231，按比例缩放，
 * 最大值 = max * (2086458231 / min)，取整输出。
 */
#include <algorithm>
#include <iostream>
using namespace std;

int main() {
    // 29 层已知数据
    int data[29][29] = {
        {7},
        {5, 8},
        {7, 8, 8},
        {9, 2, 7, 2},
        {8, 1, 4, 9, 1},
        {8, 1, 8, 8, 4, 1},
        {7, 9, 6, 1, 4, 5, 4},
        {5, 6, 5, 5, 6, 9, 5, 6},
        {5, 5, 4, 7, 9, 3, 5, 5, 1},
        {7, 5, 7, 9, 7, 4, 7, 3, 3, 1},
        {4, 6, 4, 5, 5, 8, 8, 3, 2, 4, 3},
        {1, 1, 3, 3, 1, 6, 6, 5, 5, 4, 4, 2},
        {9, 9, 9, 2, 1, 9, 1, 9, 2, 9, 5, 7, 9},
        {4, 3, 3, 7, 7, 9, 3, 6, 1, 3, 8, 8, 3, 7},
        {3, 6, 8, 1, 5, 3, 9, 5, 8, 3, 8, 1, 8, 3, 3},
        {8, 3, 2, 3, 3, 5, 5, 8, 5, 4, 2, 8, 6, 7, 6, 9},
        {8, 1, 8, 1, 8, 4, 6, 2, 2, 1, 7, 9, 4, 2, 3, 3, 4},
        {2, 8, 4, 2, 2, 9, 9, 2, 8, 3, 4, 9, 6, 3, 9, 4, 6, 9},
        {7, 9, 7, 4, 9, 7, 6, 6, 2, 8, 9, 4, 1, 8, 1, 7, 2, 1, 6},
        {9, 2, 8, 6, 4, 2, 7, 9, 5, 4, 1, 2, 5, 1, 7, 3, 9, 8, 3, 3},
        {5, 2, 1, 6, 7, 9, 3, 2, 8, 9, 5, 5, 6, 6, 6, 2, 1, 8, 7, 9, 9},
        {6, 7, 1, 8, 8, 7, 5, 3, 6, 5, 4, 7, 3, 4, 6, 7, 8, 1, 3, 2, 7, 4},
        {2, 2, 6, 3, 5, 3, 4, 9, 2, 4, 5, 7, 6, 6, 3, 2, 7, 2, 4, 8, 5, 5, 4},
        {7, 4, 4, 5, 8, 3, 3, 8, 1, 8, 6, 3, 2, 1, 6, 2, 6, 4, 6, 3, 8, 2, 9, 6},
        {1, 2, 4, 1, 3, 3, 5, 3, 4, 9, 6, 3, 8, 6, 5, 9, 1, 5, 3, 2, 6, 8, 8, 5, 3},
        {2, 2, 7, 9, 3, 3, 2, 8, 6, 9, 8, 4, 4, 9, 5, 8, 2, 6, 3, 4, 8, 4, 9, 3, 8, 8},
        {7, 7, 7, 9, 7, 5, 2, 7, 9, 2, 5, 1, 9, 2, 6, 5, 3, 9, 3, 5, 7, 3, 5, 4, 2, 8, 9},
        {7, 7, 6, 6, 8, 7, 5, 5, 8, 2, 4, 7, 7, 4, 7, 2, 6, 9, 2, 1, 8, 2, 9, 8, 5, 7, 3, 6},
        {5, 9, 4, 5, 5, 7, 5, 5, 6, 3, 5, 3, 9, 5, 8, 9, 5, 4, 1, 2, 6, 1, 4, 3, 5, 3, 2, 4, 1}};

    // 总重量数组（30 行）
    double a[30][30] = {0};

    // 初始化已知层
    for (int i = 0; i < 29; i++)
        for (int j = 0; j <= i; j++)
            a[i][j] = data[i][j];

    // 逐层向下分配重量
    for (int i = 0; i < 29; i++)
        for (int j = 0; j <= i; j++) {
            double half = a[i][j] / 2.0;
            a[i + 1][j] += half;
            a[i + 1][j + 1] += half;
        }

    // 底行 30 个电子秤读数（比例缩放前）
    double min_val = a[29][0];
    double max_val = a[29][0];
    for (int j = 1; j < 30; j++) {
        if (a[29][j] < min_val) min_val = a[29][j];
        if (a[29][j] > max_val) max_val = a[29][j];
    }

    // 按比例缩放：已知最小值对应 2086458231
    double factor = 2086458231.0 / min_val;
    long long ans = (long long)(max_val * factor);

    cout << ans << endl;
    return 0;
}
