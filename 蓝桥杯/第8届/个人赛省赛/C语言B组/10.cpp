/*
标题： k倍区间

给定一个长度为N的数列，A1, A2, ... AN，如果其中一段连续的子序列Ai, Ai+1, ... Aj(i <= j)之和是K的倍数，我们就称这个区间[i, j]是K倍区间。

你能求出数列中总共有多少个K倍区间吗？

输入
-----
第一行包含两个整数N和K。(1 <= N, K <= 100000)
以下N行每行包含一个整数Ai。(1 <= Ai <= 100000)

输出
-----
输出一个整数，代表K倍区间的数目。

例如，
输入：
5 2
1
2
3
4
5

程序应该输出：
6

资源约定：
峰值内存消耗（含虚拟机） < 256M
CPU消耗  < 2000ms


请严格按要求输出，不要画蛇添足地打印类似："请您输入..." 的多余内容。

注意：
main函数需要返回0;
只使用ANSI C/ANSI C++ 标准;
不要调用依赖于编译环境或操作系统的特殊函数。
所有依赖的函数必须明确地在源文件中 #include <xxx>
不能通过工程设置而省略常用头文件。

提交程序时，注意选择所期望的语言类型和编译器类型。
*/

/*
 * 解题思路：
 * 前缀和 + 同余计数。
 * 记 sum[i] = A1 + ... + Ai，mod[i] = sum[i] % K。
 * 若 mod[i] == mod[j-1]，则区间 [j, i] 之和为 K 的倍数。
 *
 * 统计每个余数的出现次数 cnt[r]。
 * 对每个 r，从 cnt[r] 中任选两个位置组成区间：C(cnt[r], 2) = cnt[r]*(cnt[r]-1)/2。
 * 特别地，mod[i] == 0 时，区间 [1, i] 本身就是 K 倍区间。
 * 可通过初始化 cnt[0] = 1（表示空前缀 sum[0]=0）统一处理。
 */
#include <iostream>
using namespace std;

int main() {
    int n, k;
    cin >> n >> k;

    long long cnt[100005] = {0};
    cnt[0] = 1; // 空前缀 sum[0]=0

    long long sum = 0, ans = 0;
    for (int i = 0; i < n; i++) {
        int a;
        cin >> a;
        sum += a;
        int r = sum % k;
        ans += cnt[r]; // 当前前缀与之前相同余数的每个位置构成 K 倍区间
        cnt[r]++;
    }

    cout << ans << endl;
    return 0;
}
