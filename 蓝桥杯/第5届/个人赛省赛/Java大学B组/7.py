"""

G将军有一支训练有素的军队，这个军队除开G将军外，每名士兵都有一个直接上级（可能是其他士兵，也可能是G将军）。现在G将军将接受一个特别的任务，需要派遣一部分士兵（至少一个）组成一个敢死队，为了增加敢死队队员的独立性，要求如果一名士兵在敢死队中，他的直接上级不能在敢死队中。
请问，G将军有多少种派出敢死队的方法。注意，G将军也可以作为一个士兵进入敢死队。
输入格式
输入的第一行包含一个整数n，表示包括G将军在内的军队的人数。军队的士兵从1至n编号，G将军编号为1。
接下来n-1个数，分别表示编号为2, 3, ..., n的士兵的直接上级编号，编号i的士兵的直接上级的编号小于i。
输出格式
输出一个整数，表示派出敢死队的方案数。由于数目可能很大，你只需要输出这个数除10007的余数即可。
样例输入1
3
1 1
样例输出1
4
样例说明
这四种方式分别是：
1. 选1；
2. 选2；
3. 选3；
4. 选2, 3。
样例输入2
7
1 1 2 2 3 3
样例输出2
40

数据规模与约定
对于20%的数据，n ≤ 20；
对于40%的数据，n ≤ 100；
对于100%的数据，1 ≤ n ≤ 100000。


资源约定：
峰值内存消耗（含虚拟机） < 256M
CPU消耗  < 2000ms


请严格按要求输出，不要画蛇添足地打印类似：“请您输入...” 的多余内容。

所有代码放在同一个源文件中，调试通过后，拷贝提交该源码。
注意：不要使用package语句。不要使用jdk1.7及以上版本的特性。
注意：主类的名字必须是：Main，否则按无效代码处理。


"""

import sys

MOD = 10007


def main():
    data = sys.stdin.read().split()
    idx = 0
    n = int(data[idx])
    idx += 1

    children = [[] for _ in range(n + 1)]
    for i in range(2, n + 1):
        p = int(data[idx])
        idx += 1
        children[p].append(i)

    # f[u]: u被选中时，子树的方案数
    # g[u]: u未被选中时，子树的方案数
    f = [0] * (n + 1)
    g = [0] * (n + 1)

    # 利用parent[i] < i的性质，从n到1逆序处理，保证子节点先于父节点
    for u in range(n, 0, -1):
        if not children[u]:
            f[u] = 1
            g[u] = 1
        else:
            fu = 1
            gu = 1
            for v in children[u]:
                fu = fu * g[v] % MOD
                gu = gu * (f[v] + g[v]) % MOD
            f[u] = fu
            g[u] = gu

    # 总方案 = f[1] + g[1] - 1（减去全不选的空集）
    print((f[1] + g[1] - 1) % MOD)


if __name__ == "__main__":
    main()
