/*

标题：年龄问题

s夫人一向很神秘。这会儿有人问起她的年龄，她想了想说：
"20年前，我丈夫的年龄刚好是我的2倍，而现在他的年龄刚好是我的1.5倍"。

你能算出s夫人现在的年龄吗？

注意，需要提交的是一个整数，不要填写任何多余的内容。

*/

#include <iostream>
using namespace std;

int main() {
    // 设s夫人现在年龄为 x，则：
    // 20年前：s夫人 x-20 岁，丈夫 2(x-20) 岁
    // 现在：丈夫 1.5x 岁 = 2(x-20) + 20
    // 1.5x = 2x - 40 + 20 => 0.5x = 20 => x = 40
    cout << 40 << endl;
    return 0;
}
