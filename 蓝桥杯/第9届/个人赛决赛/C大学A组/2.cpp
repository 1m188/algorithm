/*

标题：阅兵方阵

x国要参加同盟阅兵活动。
主办方要求每个加盟国派出的士兵恰好能组成 2 个方阵。
x国发现弱小的 y国派出了130人的队伍，他们的士兵在行进中可以变换2种队形：
 130 = 81 + 49 = 9^2 + 7^2
 130 = 121 + 9 = 11^2 + 3^2

x国君很受刺激，觉得x国面积是y国的6倍，理应变出更多队形。
于是他发号施令：
我们要派出一支队伍，在行进中要变出 12 种队形！！！

手下人可惨了，要忙着计算至少多少人才能组成 12 种不同的双方阵。
请你利用计算机的优势来计算一下，至少需要多少士兵。

（ps: 不要失去信心，1105人就能组成4种队形了）

注意，需要提交的是一个整数，表示至少需要士兵数目，不要填写任何多余的内容。

*/

#include <stdio.h>
#include <string.h>

#define MAX_N 200000

int main() {
    static int cnt[MAX_N];

    // 枚举 a > b > 0，统计 N = a² + b² 的表示方案数
    for (int a = 1; a < 500; a++) {
        for (int b = 1; b < a; b++) {
            int n = a * a + b * b;
            if (n < MAX_N) {
                cnt[n]++;
            }
        }
    }

    // 找到第一个恰好有 12 种表示的数
    for (int i = 1; i < MAX_N; i++) {
        if (cnt[i] == 12) {
            printf("%d\n", i);
            break;
        }
    }

    return 0;
}
