/*

 标题：激光样式

 x星球的盛大节日为增加气氛，用30台机光器一字排开，向太空中打出光柱。
 安装调试的时候才发现，不知什么原因，相邻的两台激光器不能同时打开！
 国王很想知道，在目前这种bug存在的情况下，一共能打出多少种激光效果？

 显然，如果只有3台机器，一共可以成5种样式，即：
 全都关上（sorry, 此时无声胜有声，这也算一种）
 开一台，共3种
 开两台，只1种

 30台就不好算了，国王只好请你帮忙了。

 要求提交一个整数，表示30台激光器能形成的样式种数。

 注意，只提交一个整数，不要填写任何多余的内容。

 */

// 递推公式：f(n) = f(n-1) + f(n-2)
// 解释：
//   第 n 台关闭 -> 前 n-1 台有 f(n-1) 种
//   第 n 台打开 -> 第 n-1 台必须关，前 n-2 台有 f(n-2) 种
// 边界：f(0) = 1（0 台全关），f(1) = 2（全关 / 开第 1 台）
// 验证：f(3) = 5，与题目一致
// 答案：f(30) = Fib(32) = 2178309

#include <iostream>
using namespace std;

int main() {
    int a = 1; // f(0)
    int b = 2; // f(1)
    for (int i = 2; i <= 30; ++i) {
        int c = a + b;
        a = b;
        b = c;
    }
    cout << b << endl;
    return 0;
}
