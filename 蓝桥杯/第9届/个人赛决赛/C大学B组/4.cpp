/*
 * 解题思路：
 * 将 n 个分钟数 (0..n-1) 看作图的顶点。
 * 从 u 可以走到 (u+1)%n 和 (u+k)%n，边权均为 1（按一次按钮）。
 * 由于图是循环群 Z_n 关于生成元 {1,k} 的 Cayley 图，具有顶点传递性，
 * 从任意起点到任意终点的距离分布等同于从 0 出发到所有顶点的距离分布。
 * 因此只需 BFS 从 0 出发，计算到所有顶点的最短距离，取最大值即为答案。
 * 时间 O(n)，空间 O(n)，n <= 100000，满足限制。
 */

#include <iostream>
#include <queue>
#include <vector>
using namespace std;

int main() {
    int n, k;
    cin >> n >> k;

    // dist[i] = -1 表示未访问
    vector<int> dist(n, -1);
    queue<int> q;

    dist[0] = 0;
    q.push(0);

    while (!q.empty()) {
        int u = q.front();
        q.pop();

        // 按 +1 按钮
        int v1 = (u + 1) % n;
        if (dist[v1] == -1) {
            dist[v1] = dist[u] + 1;
            q.push(v1);
        }

        // 按 +k 按钮
        int vk = (u + k) % n;
        if (dist[vk] == -1) {
            dist[vk] = dist[u] + 1;
            q.push(vk);
        }
    }

    // 求最大距离
    int ans = 0;
    for (int i = 0; i < n; i++) {
        if (dist[i] > ans) ans = dist[i];
    }

    cout << ans << endl;
    return 0;
}
