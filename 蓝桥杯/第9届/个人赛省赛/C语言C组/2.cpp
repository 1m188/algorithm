/*

标题：猴子分香蕉

5只猴子是好朋友，在海边的椰子树上睡着了。这期间，有商船把一大堆香蕉忘记在沙滩上离去。
第1只猴子醒来，把香蕉均分成5堆，还剩下1个，就吃掉并把自己的一份藏起来继续睡觉。
第2只猴子醒来，重新把香蕉均分成5堆，还剩下2个，就吃掉并把自己的一份藏起来继续睡觉。
第3只猴子醒来，重新把香蕉均分成5堆，还剩下3个，就吃掉并把自己的一份藏起来继续睡觉。
第4只猴子醒来，重新把香蕉均分成5堆，还剩下4个，就吃掉并把自己的一份藏起来继续睡觉。
第5只猴子醒来，重新把香蕉均分成5堆，哈哈，正好不剩！

请计算一开始最少有多少个香蕉。

需要提交的是一个整数，不要填写任何多余的内容。


*/

#include <cstdio>
int main() {
    // 暴力枚举：模拟5次分香蕉过程，找到第一个满足条件的数
    // 每只猴子分得的"一份"必须是正整数（即当前香蕉足够分成有意义的5堆）
    for (int n = 1;; ++n) {
        int bananas = n;
        int ok = 1;
        for (int i = 1; i <= 5; ++i) {
            // 第i只猴子分香蕉：余数 = i（第5只为0）
            int remainder = i % 5;
            if (bananas % 5 != remainder) {
                ok = 0;
                break;
            }
            int pile = (bananas - remainder) / 5; // 每堆数量
            if (pile == 0) {                      // 每堆至少要有1个香蕉才有意义
                ok = 0;
                break;
            }
            // 吃掉余数，藏起1份(pile个)，剩余4份
            bananas = pile * 4;
        }
        if (ok) {
            printf("%d\n", n);
            break;
        }
    }
    return 0;
}
