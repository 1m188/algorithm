#include <stdio.h>
#include <string.h>

#define MAX_N 305

int main() {
    int n;
    scanf("%d", &n);

    int width = 2 * n - 1;
    int total = 4 * n - 4;

    // 生成由自然数拼接而成的足够长的数字串
    char digits[MAX_N * 5] = {0};
    int pos = 0;
    for (int num = 1; pos < total; num++) {
        char buf[12];
        sprintf(buf, "%d", num);
        int len = strlen(buf);
        for (int j = 0; j < len && pos < total; j++) {
            digits[pos++] = buf[j];
        }
    }
    digits[pos] = '\0';

    // 创建网格，初始全用 '.' 填充
    char grid[MAX_N][MAX_N * 2];
    for (int i = 0; i < n; i++) {
        for (int j = 0; j < width; j++) {
            grid[i][j] = '.';
        }
        grid[i][width] = '\0';
    }

    int idx = 0;
    // 1. 顶点
    grid[0][n - 1] = digits[idx++];
    // 2. 左边向下（含左下角）
    for (int i = 1; i <= n - 1; i++) {
        grid[i][n - 1 - i] = digits[idx++];
    }
    // 3. 底边向右（跳过左下角 col 0，已填充）
    for (int j = 1; j <= 2 * n - 2; j++) {
        grid[n - 1][j] = digits[idx++];
    }
    // 4. 右边向上（从倒数第二行到第二行）
    for (int i = n - 2; i >= 1; i--) {
        grid[i][n - 1 + i] = digits[idx++];
    }

    // 输出：每行截断到右边缘列（n-1+i），去除尾随空格
    for (int i = 0; i < n; i++) {
        grid[i][n + i] = '\0';
        printf("%s\n", grid[i]);
    }

    return 0;
}
