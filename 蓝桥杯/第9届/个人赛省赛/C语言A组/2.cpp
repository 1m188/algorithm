/*

标题：星期一

整个20世纪（1901年1月1日至2000年12月31日之间），一共有多少个星期一？
(不要告诉我你不知道今天是星期几)

注意：需要提交的只是一个整数，不要填写任何多余的内容或说明文字。

*/

/*
 * 分析：
 *   1901年1月1日是星期二。
 *   20世纪包含 1901 年至 2000 年，共 100 年。
 *   闰年：能被4整除的年份，共 25 个（1904, 1908, ..., 2000）。
 *   总天数 = 100 × 365 + 25 = 36525 天。
 *   36525 ÷ 7 = 5217 周 余 6 天。
 *   余下的 6 天依次为：周二、周三、周四、周五、周六、周日。
 *   这 6 天中没有星期一，所以答案就是 5217。
 */

#include "iostream"

int main() {
    std::cout << 5217 << std::endl;
    return 0;
}
