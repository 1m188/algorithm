/*
 * 分析：
 *   设飞行时间为 F（秒），两地时差为 D（东正西负）。
 *   去程：arrB - depA = F + D
 *   回程：arrA - depB = F - D
 *   两式相加消去 D：2F = (arrB - depA) + (arrA - depB)
 *   即：飞行时间 = (去程表观时长 + 回程表观时长) / 2
 *
 *   解析时需注意可选的 (+d) 后缀表示跨 d 天，表观时长应加上 d×24 小时。
 */

#include "cstdio"
#include "cstring"

// 解析一行起降时间，返回表观时长（秒）
int parseDuration() {
    char line[128];
    if (fgets(line, sizeof(line), stdin) == NULL) return 0;

    int h1, m1, s1, h2, m2, s2;
    int day = 0;

    // 检查是否有 (+d) 后缀
    char *p = strstr(line, "(+");
    if (p != NULL) {
        sscanf(p, "(+%d)", &day);
        *p = '\0'; // 截断后缀，便于后续解析
    }

    sscanf(line, "%d:%d:%d %d:%d:%d", &h1, &m1, &s1, &h2, &m2, &s2);

    int dep = h1 * 3600 + m1 * 60 + s1;
    int arr = h2 * 3600 + m2 * 60 + s2 + day * 24 * 3600;
    return arr - dep;
}

int main() {
    int T;
    (void)!scanf("%d", &T);
    getchar(); // 消耗 T 后的换行符

    while (T--) {
        int dur1 = parseDuration(); // 去程表观时长
        int dur2 = parseDuration(); // 回程表观时长
        int flight = (dur1 + dur2) / 2;

        printf("%02d:%02d:%02d\n",
               flight / 3600,
               (flight % 3600) / 60,
               flight % 60);
    }

    return 0;
}
