/*

标题：测试次数

x星球的居民脾气不太好，但好在他们生气的时候唯一的异常举动是：摔手机。
各大厂商也就纷纷推出各种耐摔型手机。x星球的质监局规定了手机必须经过耐摔测试，并且评定出一个耐摔指数来，之后才允许上市流通。

x星球有很多高耸入云的高塔，刚好可以用来做耐摔测试。塔的每一层高度都是一样的，与地球上稍有不同的是，他们的第一层不是地面，而是相当于我们的2楼。

如果手机从第7层扔下去没摔坏，但第8层摔坏了，则手机耐摔指数=7。
特别地，如果手机从第1层扔下去就坏了，则耐摔指数=0。
如果到了塔的最高层第n层扔没摔坏，则耐摔指数=n

为了减少测试次数，从每个厂家抽样3部手机参加测试。

某次测试的塔高为1000层，如果我们总是采用最佳策略，在最坏的运气下最多需要测试多少次才能确定手机的耐摔指数呢？

请填写这个最多测试次数。

注意：需要填写的是一个整数，不要填写任何多余内容。


*/

/*
 * 分析：
 *   经典"鸡蛋掉落"问题变种：3 部手机，1000 层塔。
 *   设 f[m][k] 为用 m 部手机、最多测试 k 次能确定的最大楼层数。
 *   递推公式：f[m][k] = f[m-1][k-1] + f[m][k-1] + 1
 *     - 本次摔坏 → 向下用 m-1 部手机、k-1 次继续
 *     - 本次未坏 → 向上用 m 部手机、k-1 次继续
 *     - +1 为当前测试的这一层
 *   边界：f[1][k] = k；f[m][0] = 0
 *   求最小 k 使得 f[3][k] >= 1000 → 答案 k = 19
 */

#include "iostream"

int main() {
    const int M = 3;       // 手机数量
    const int N = 1000;    // 塔高
    const int MAX_K = 100; // 足够大的 k

    int f[M + 1][MAX_K + 1] = {0};

    for (int k = 1; k <= MAX_K; k++) {
        for (int m = 1; m <= M; m++) {
            // 递推公式
            f[m][k] = f[m - 1][k - 1] + f[m][k - 1] + 1;
        }

        // 找到最小 k 使得 f[3][k] >= N
        if (f[M][k] >= N) {
            std::cout << k << std::endl;
            break;
        }
    }

    return 0;
}
