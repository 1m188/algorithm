/*

标题：测试次数

x星球的居民脾气不太好，但好在他们生气的时候唯一的异常举动是：摔手机。
各大厂商也就纷纷推出各种耐摔型手机。x星球的质监局规定了手机必须经过耐摔测试，并且评定出一个耐摔指数来，之后才允许上市流通。

x星球有很多高耸入云的高塔，刚好可以用来做耐摔测试。塔的每一层高度都是一样的，与地球上稍有不同的是，他们的第一层不是地面，而是相当于我们的2楼。

如果手机从第7层扔下去没摔坏，但第8层摔坏了，则手机耐摔指数=7。
特别地，如果手机从第1层扔下去就坏了，则耐摔指数=0。
如果到了塔的最高层第n层扔没摔坏，则耐摔指数=n

为了减少测试次数，从每个厂家抽样3部手机参加测试。

某次测试的塔高为1000层，如果我们总是采用最佳策略，在最坏的运气下最多需要测试多少次才能确定手机的耐摔指数呢？

请填写这个最多测试次数。

注意：需要填写的是一个整数，不要填写任何多余内容。



*/

public class _4 {

    public static void main(String[] args) {
        /*
         * 题目转化为经典的“鸡蛋掉落”问题（egg dropping）：
         * 给定3部手机和1000层塔，在最坏运气下，最少需要多少次测试
         * 才能确定手机的耐摔指数。
         *
         * 设 f(m, k) 表示在最多 m 次测试和 k 部手机的条件下，
         * 最多可以检测出多少层楼的耐摔指数。
         *
         * 递推关系：
         * 在第 x 层扔手机，有两种结果：
         * - 手机摔坏了：剩余 m-1 次测试，k-1 部手机，可检测 f(m-1, k-1) 层
         * - 手机没摔坏：剩余 m-1 次测试，k 部手机，可检测 f(m-1, k) 层
         * 因此：f(m, k) = f(m-1, k-1) + f(m-1, k) + 1
         *
         * 边界条件：
         * - f(m, 1) = m：只有1部手机时，只能从下往上逐层测试，m次最多测m层
         * - f(1, k) = 1：只有1次测试机会时，最多只能测1层
         * - f(0, k) = 0, f(m, 0) = 0
         */

        int floors = 1000;
        int phones = 3;

        /*
         * 从 m=1 开始递增计算，找到最小的 m 使得 f(m, 3) >= 1000。
         * 只需要维护两行状态（上一轮和当前轮），节省空间。
         */
        int m = 0;
        /* dp[k] 表示当前测试次数 m 下，k 部手机能检测的最大楼层数 */
        long[] dp = new long[phones + 1];

        /*
         * 逐步增加测试次数 m，直到 dp[phones] >= floors。
         * dp[phones] 的值就是 f(m, phones)。
         */
        while (dp[phones] < floors) {
            m++;
            /*
             * 从大到小更新 dp[k]，避免覆盖。
             * dp[k] = dp[k] + dp[k-1] + 1，其中等号左边的 dp[k] 和 dp[k-1]
             * 分别对应上一轮的 f(m-1, k) 和 f(m-1, k-1)。
             */
            for (int k = phones; k >= 1; k--) {
                dp[k] = dp[k] + dp[k - 1] + 1;
            }
        }

        System.out.println(m);
    }
}
