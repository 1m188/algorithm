import java.util.Scanner;

/**
 * 螺旋折线
 *
 * 平面上的螺旋从原点 (0,0) 出发，按 左→上→右→下 的顺序逐圈向外扩展。
 * 第 n 圈（n≥1）四条边步长依次为：2n-1（左）、2n-1（上）、2n（右）、2n（下）。
 *
 * 第 n 圈起点为 (n-1, -(n-1))，前 n-1 圈的累计步数 S(n) = 4(n-1)² + 2(n-1)。
 *
 * 四条边的点集与相对起点的偏移：
 * 左（←）：y = -(n-1), x ∈ [-n, n-1] 偏移 = (n-1) - x
 * 上（↑）：x = -n, y ∈ [-(n-1), n] 偏移 = (2n-1) + (y + n - 1)
 * 右（→）：y = n, x ∈ [-n, n] 偏移 = (4n-2) + (x + n)
 * 下（↓）：x = n, y ∈ [-n, n] 偏移 = (6n-2) + (n - y)
 *
 * 根据边方程反解圈数 n：
 * 右：n = y (y≥0 且 |x|≤y)
 * 下：n = x (x≥0 且 |y|≤x)
 * 上：n = -x (x<0 且 x+1≤y≤-x)
 * 左：n = 1-y (y<0 且 y-1≤x≤-y)
 */
public class _7 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        long x = sc.nextLong();
        long y = sc.nextLong();
        sc.close();

        // 原点单独处理
        if (x == 0 && y == 0) {
            System.out.println(0);
            return;
        }

        long n;
        long ax = Math.abs(x);
        long ay = Math.abs(y);

        /*
         * 定位点在第 n 圈的哪条边上。
         * 四个区域互不重叠（角点在边界上属于两侧，但两侧公式给出相同结果）。
         */
        if (y >= ax) {
            // 右边：y = n, |x| <= n
            n = y;
        } else if (x >= ay) {
            // 下边：x = n, |y| <= n
            n = x;
        } else if (x < 0 && x + 1 <= y && y <= -x) {
            // 上边：x = -n, -(n-1) <= y <= n
            n = -x;
        } else {
            // 左边：y = -(n-1), -n <= x <= n-1
            n = 1 - y;
        }

        // 前 n-1 圈总步数 S
        long s = 4 * (n - 1) * (n - 1) + 2 * (n - 1);

        long ans;
        /*
         * 根据点在四条边上的位置计算圈内偏移，累加到 S 上。
         * 条件需包含坐标范围，避免角点被误分到相邻边。
         */
        if (y == -(n - 1) && x < n) {
            // 左边：y = -(n-1), x ∈ [-n, n-1]
            ans = s + (n - 1) - x;
        } else if (x == -n && y < n) {
            // 上边：x = -n, y ∈ [-(n-1), n)
            ans = s + (2 * n - 1) + (y + n - 1);
        } else if (y == n && x < n) {
            // 右边：y = n, x ∈ [-n, n)
            ans = s + (4 * n - 2) + (x + n);
        } else {
            // 下边：x = n, y ∈ [-n, n]
            ans = s + (6 * n - 2) + (n - y);
        }

        System.out.println(ans);
    }
}
