/*

标题：方格计数

如图p1.png所示，在二维平面上有无数个1x1的小方格。


我们以某个小方格的一个顶点为圆心画一个半径为1000的圆。
你能计算出这个圆里有多少个完整的小方格吗？ 

注意：需要提交的是一个整数，不要填写任何多余内容。

*/

public class _2 {

    public static void main(String[] args) {
        /*
         * 圆的半径。题目中给出的半径为1000。
         * 使用long类型防止R*R溢出（虽然1000*1000=1000000在int范围内，
         * 但使用long更安全，且便于扩展更大的R值）。
         */
        long R = 1000;

        /*
         * quarterCount 表示一个“象限”内完全在圆内的单位正方形数量。
         *
         * 分析：圆心位于网格顶点(0,0)。一个单位正方形[i, i+1]×[j, j+1]
         * 要完全在圆内，其所有4个顶点都必须在圆内。
         * 对于i≥0, j≥0的情况，离圆心最远的顶点是(i+1, j+1)，
         * 因此条件是：(i+1)² + (j+1)² ≤ R²。
         *
         * 令 x = i+1, y = j+1，则 x, y ≥ 1，条件变为 x² + y² ≤ R²。
         * 对于每个固定的x，满足条件的y的个数为 floor(√(R² - x²))。
         * 由于圆的对称性，四个“象限”内的正方形数量相同，
         * 最终结果 = 4 × quarterCount。
         */
        long quarterCount = 0;
        for (long x = 1; x <= R; x++) {
            /*
             * 计算当前x对应的最大y值，使得 x² + y² ≤ R²。
             * 即 y ≤ √(R² - x²)，所以最大y值为 floor(√(R² - x²))。
             *
             * 为避免浮点精度问题（例如 sqrt 可能返回略小于整数的值），
             * 加入极小量 1e-9 确保 floor 结果正确。
             */
            long yMax = (long) (Math.sqrt(R * R - x * x) + 1e-9);
            quarterCount += yMax;
        }

        /*
         * 四个象限对称，总数 = 4 × quarterCount。
         * 输出结果为整数，直接打印即可。
         */
        System.out.println(4 * quarterCount);
    }
}
