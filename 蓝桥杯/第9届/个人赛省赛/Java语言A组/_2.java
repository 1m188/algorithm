/*
 
标题：星期一

整个20世纪（1901年1月1日至2000年12月31日之间），一共有多少个星期一？
(不要告诉我你不知道今天是星期几)

注意：需要提交的只是一个整数，不要填写任何多余的内容或说明文字。

*/

public class _2 {
    public static void main(String[] args) {
        // 1900年1月1日是星期一（已知事实）
        // 1900年不是闰年（被100整除但不被400整除），共365天
        // 365 ≡ 1 (mod 7)，所以1901年1月1日是星期二

        // 1901-2000年间闰年数：1904, 1908, ..., 2000
        // 共 (2000 - 1904) / 4 + 1 = 25 个
        int leapYears = 25;
        int totalDays = 100 * 365 + leapYears; // 36525

        // 36525 ÷ 7 = 5217 周余 6 天
        // 余数6天：星期二 ~ 星期日，不包含星期一
        int mondays = totalDays / 7; // 5217

        System.out.println(mondays);
    }
}
