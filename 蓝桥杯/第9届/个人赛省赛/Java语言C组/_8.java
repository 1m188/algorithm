import java.util.*;

public class _8 {

    /**
     * 等腰三角形
     *
     * 输入三角形高度 n（3 < n < 300），输出由自然数拼接串填充的等腰三角形。
     * 空格一律用 "." 代替。
     *
     * 核心思路：
     * 1. 生成填充字符串：将自然数 1, 2, 3, ... 依次拼接，
     * 直到长度达到三角形所需的总字符数（4n - 4）。
     * 2. 按逆时针方向将字符填入三角形三边：
     * a) 左腰：从顶点 (0, n-1) 沿左下方到 (n-1, 0)
     * b) 底边：从 (n-1, 1) 沿右方到 (n-1, 2n-2)
     * c) 右腰：从 (n-2, 2n-3) 沿右上方到 (1, n)
     * 3. 其余位置用 '.' 填充，逐行输出。
     *
     * 时间复杂度：O(n²)，生成约 4n 个字符，填充 n×(2n-1) 矩阵。
     * 空间复杂度：O(n²)，存储三角形矩阵。
     */

    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int n = sc.nextInt();
        sc.close();

        /* 三角形总宽度为 2n-1 */
        int cols = 2 * n - 1;
        /* 所需字符总数 = 4n - 4 */
        int totalChars = 4 * n - 4;

        /*
         * 第一步：生成填充用的数字字符串
         * 将自然数 1, 2, 3, ... 拼接，直到长度 ≥ totalChars
         */
        StringBuilder digits = new StringBuilder();
        int num = 1;
        while (digits.length() < totalChars) {
            digits.append(num);
            num++;
        }
        String s = digits.toString();

        /*
         * 第二步：创建三角形字符矩阵，初始全为 '.'
         */
        char[][] triangle = new char[n][cols];
        for (int i = 0; i < n; i++) {
            for (int j = 0; j < cols; j++) {
                triangle[i][j] = '.';
            }
        }

        int pos = 0; // 当前在字符串 s 中的索引

        /*
         * 第三步：逆时针填充三边
         *
         * a) 左腰（从上到下）：
         * 第 i 行的左腰列坐标为 n-1-i（顶点在中间，逐行左移）
         */
        for (int i = 0; i < n; i++) {
            triangle[i][n - 1 - i] = s.charAt(pos++);
        }

        /*
         * b) 底边（从左到右）：
         * 第 n-1 行，从列 1 到列 2n-2（列 0 已在左腰填充）
         */
        for (int j = 1; j < cols; j++) {
            triangle[n - 1][j] = s.charAt(pos++);
        }

        /*
         * c) 右腰（从下到上）：
         * 第 i 行的右腰列坐标为 n-1+i，
         * 从倒数第二行向上到第二行（首行和末行已填充）
         */
        for (int i = n - 2; i >= 1; i--) {
            triangle[i][n - 1 + i] = s.charAt(pos++);
        }

        /*
         * 第四步：逐行输出三角形
         * 注意：每行只输出到最右侧非 '.' 字符为止（无尾随多余字符），
         * 这样才能与题目示例完全一致。
         *
         * 第 i 行的有效范围：
         * i = 0: 0 .. n-1 （仅顶点）
         * 1 ≤ i < n-1: 0 .. n-1+i （左腰+右腰）
         * i = n-1: 0 .. 2n-2 （完整底边）
         */
        for (int i = 0; i < n; i++) {
            int rightmost;
            if (i == 0) {
                rightmost = n - 1;
            } else if (i == n - 1) {
                rightmost = cols - 1;
            } else {
                rightmost = n - 1 + i;
            }
            for (int j = 0; j <= rightmost; j++) {
                System.out.print(triangle[i][j]);
            }
            System.out.println();
        }
    }
}
