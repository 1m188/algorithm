/*

标题：缩位求和

在电子计算机普及以前，人们经常用一个粗略的方法来验算四则运算是否正确。
比如：248 * 15 = 3720
把乘数和被乘数分别逐位求和，如果是多位数再逐位求和，直到是1位数，得
2 + 4 + 8 = 14 ==> 1 + 4 = 5;
1 + 5 = 6
5 * 6
而结果逐位求和为 3
5 * 6 的结果逐位求和与3符合，说明正确的可能性很大！！（不能排除错误）

请你写一个计算机程序，对给定的字符串逐位求和：
输入为一个由数字组成的串，表示n位数(n<1000);
输出为一位数，表示反复逐位求和的结果。

例如：
输入：
35379

程序应该输出：
9

再例如：
输入：
7583676109608471656473500295825

程序应该输出：
1


资源约定：
峰值内存消耗（含虚拟机） < 256M
CPU消耗  < 1000ms


请严格按要求输出，不要画蛇添足地打印类似：“请您输入...” 的多余内容。

所有代码放在同一个源文件中，调试通过后，拷贝提交该源码。
不要使用package语句。不要使用jdk1.7及以上版本的特性。
主类的名字必须是：Main，否则按无效代码处理。

*/

import java.util.*;

public class _7 {

    /**
     * 缩位求和（数字根）
     *
     * 给定一个由数字组成的字符串（长度 < 1000），反复将各位数字相加，
     * 直到结果为一位数，输出该结果。
     *
     * 数学性质：
     * 一个正整数的"数字根"（digital root）满足：
     * dr(n) = 1 + (n - 1) % 9 （当 n > 0）
     * dr(0) = 0
     * 因为模 9 运算在逐位相加时保持不变。
     *
     * 算法（两步法）：
     * 1. 一次遍历求所有数字之和 total（最大约 9000，在 int 范围内）。
     * 2. 对 total 应用数字根公式得到最终一位数。
     * 此方法避免了对巨大整数的处理，且时间复杂度 O(n)。
     *
     * 时间复杂度：O(n)，n 为字符串长度。
     * 空间复杂度：O(1)，仅需存储字符串和总和。
     */

    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        String s = sc.next();
        sc.close();

        /*
         * 第一步：计算所有数位的总和。
         * 由于输入全为数字字符，每个字符 c 对应的数值为 c - '0'。
         */
        int total = 0;
        for (int i = 0; i < s.length(); i++) {
            total += s.charAt(i) - '0';
        }

        /*
         * 第二步：对总和应用数字根公式。
         * 注意特判 total == 0（输入为 "0" 或一串零）。
         */
        int result;
        if (total == 0) {
            result = 0;
        } else {
            result = (total - 1) % 9 + 1;
        }

        System.out.println(result);
    }
}
