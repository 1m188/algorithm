/*

标题：猴子分香蕉

5只猴子是好朋友，在海边的椰子树上睡着了。这期间，有商船把一大堆香蕉忘记在沙滩上离去。
第1只猴子醒来，把香蕉均分成5堆，还剩下1个，就吃掉并把自己的一份藏起来继续睡觉。
第2只猴子醒来，重新把香蕉均分成5堆，还剩下2个，就吃掉并把自己的一份藏起来继续睡觉。
第3只猴子醒来，重新把香蕉均分成5堆，还剩下3个，就吃掉并把自己的一份藏起来继续睡觉。
第4只猴子醒来，重新把香蕉均分成5堆，还剩下4个，就吃掉并把自己的一份藏起来继续睡觉。
第5只猴子醒来，重新把香蕉均分成5堆，哈哈，正好不剩！

请计算一开始最少有多少个香蕉。

需要提交的是一个整数，不要填写任何多余的内容。


*/

public class _2 {

    /**
     * 猴子分香蕉
     *
     * 从最后一只（第5只）猴子逆向递推：
     * 设第5只猴子分完后每堆有 a5 个，则第5只猴子看到的香蕉数 S5 = 5×a5。
     *
     * 往前逆推，第 i 只猴子（i=4,3,2,1）：
     * - 该猴子藏起 a_i 个后剩 S_{i+1} = 4×a_i
     * - 所以 a_i = S_{i+1} / 4（必须整除）
     * - 该猴子看到的总数 S_i = 5×a_i + i（i=4 剩4，i=3 剩3，...）
     *
     * 从 a5 = 1 开始向上枚举，找到第一个能使所有逆推都整除的 a5，
     * 此时的 S1 即为最小初始香蕉数。
     *
     * 解法更直接的思路：从最小可能的 S1 向上暴力枚举模拟分香蕉过程，
     * 验证每一步是否符合条件。此处使用逆向递推，效率更高。
     */

    public static void main(String[] args) {
        /*
         * 从 a5 = 1 开始枚举第5只猴子分得的每堆香蕉数，
         * 逆向递推到第1只猴子，检查每一步能否整除。
         */
        int a5 = 0;
        while (true) {
            a5++;
            int s5 = 5 * a5; // 第5只猴子醒来看到的香蕉数

            /* 递推到第4只猴子：余4 */
            if (s5 % 4 != 0)
                continue;
            int a4 = s5 / 4;
            int s4 = 5 * a4 + 4;

            /* 递推到第3只猴子：余3 */
            if (s4 % 4 != 0)
                continue;
            int a3 = s4 / 4;
            int s3 = 5 * a3 + 3;

            /* 递推到第2只猴子：余2 */
            if (s3 % 4 != 0)
                continue;
            int a2 = s3 / 4;
            int s2 = 5 * a2 + 2;

            /* 递推到第1只猴子：余1 */
            if (s2 % 4 != 0)
                continue;
            int a1 = s2 / 4;
            int s1 = 5 * a1 + 1; // 初始香蕉总数

            System.out.println(s1);
            break;
        }
    }
}
