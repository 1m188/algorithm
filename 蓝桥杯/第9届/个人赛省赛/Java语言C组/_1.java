/*

标题：哪天返回

小明被不明势力劫持。后被扔到x星站再无问津。小明得知每天都有飞船飞往地球，但需要108元的船票，而他却身无分文。
他决定在x星战打工。好心的老板答应包食宿，第1天给他1元钱。
并且，以后的每一天都比前一天多2元钱，直到他有足够的钱买票。
请计算一下，小明在第几天就能凑够108元，返回地球。

要求提交的是一个整数，表示第几天。请不要提交任何多余的内容。

*/

public class _1 {

    /**
     * 哪天返回
     *
     * 小明第 i 天获得 1 + 2×(i-1) = 2i - 1 元。
     * 前 n 天的总收入构成等差数列，和为 n² 元。
     * 令 n² ≥ 108，解得 n ≥ √108 ≈ 10.39，因此 n = 11。
     *
     * 验证：S₁₀ = 100 < 108，S₁₁ = 121 ≥ 108。
     */

    public static void main(String[] args) {
        /*
         * 等差数列通项：a_i = 1 + 2*(i-1)
         * 前 n 项和：S_n = n*(1 + (2n-1))/2 = n²
         *
         * 循环累加至总和 >= 108，记录所需天数。
         */
        int target = 108; // 船票金额
        int day = 0; // 当前天数
        int total = 0; // 累计收入

        while (total < target) {
            day++;
            total += 2 * day - 1; // 第 day 天收入为 2*day - 1
        }

        System.out.println(day); // 输出结果：11
    }
}
